public class Task1a {
    public static int addingNumbers(int numberA, int numberB) {
        return numberA + numberB;
    }

    public static int subtractingNumbers(int numberA, int numberB) {
        return numberA - numberB;
    }

    public static int multiplyingNumbers(int numberA, int numberB) {
        return numberA * numberB;
    }

    public static int dividingNumbers(int numberA, int numberB) {
        return numberA / numberB;
    }
}
